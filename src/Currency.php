    private static $currenciesLoaded = [];

    /**
     * @var string
     */
    private $name;

    /**
     * @var array|Carbon[]
     */
    private $conversions;

    /**
     * @var array|float[]
     */
    private $rates;

    /**
     * Currency constructor.
     *
     * @param string $name
     */
    private function __construct(string $name)
    {
        $this->name        = $name;
        $this->conversions = [];
        $this->rates       = [];
    }

    /**
     * @param string $name
     *
     * @return Currency
     * @throws InvalidCurrencyException
     */
    public static function get(string $name): Currency
    {
        self::checkCurrency($name);
        self::$currenciesLoaded[$name] = self::$currenciesLoaded[$name] ?? new self($name);

        return self::$currenciesLoaded[$name];
    }

    /**
     * @return string
     */
    public function getName(): string
    {
        return $this->name;
    }

    /**
     * This class have a 15 minutes cache system
     *
     * @param Currency $currency
     *
     * @return float
     * @throws RuntimeException
     */
    public function getConversion(Currency $currency): float
    {
        if (($this->conversions[$currency->getName()] ?? false) && ($this->rates[$currency->getName()] ?? false)) {
            $date = $this->conversions[$currency->getName()];

            if ($date->diffInSeconds() <= 900) {
                return $this->rates[$currency->getName()];
            }
        }

        $rate = $this->getConversions([$currency])[$currency->getName()];

        $this->conversions[$currency->getName()] = new Carbon();
        $this->rates[$currency->getName()]       = $rate;

        return $rate;
    }

    /**
     * @param array|Currency[] $currencies
     *
     * @return array
     * @throws RuntimeException
     */
    public function getConversions(array $currencies): array
    {
        $currencies = \array_slice($currencies, 0, 20);

        $list = [];
        foreach ($currencies as $currency) {
            $list[] = $currency->getName();
        }

        $client   = new Client(['base_uri' => 'https://min-api.cryptocompare.com']);
        $response = $client->request('GET', '/data/price', [
            'query' => [
                'fsym'  => $this->name,
                'tsyms' => \implode(',', $list)
            ]
        ]);

        return \json_decode($response->getBody()->getContents(), true);
    }

    /**
     * @param string $currency
     *
     * @return bool
     */
    public static function existsCurrency(string $currency): bool
    {
        return isset(self::CURRENCIES[$currency]);
    }

    /**
     * @param string $currency
     *
     * @throws InvalidCurrencyException
     */
    public static function checkCurrency(string $currency): void
    {
        if (Intl::getCurrencyBundle()->getCurrencyName($currency)) {
            return;
        }

        if (!self::existsCurrency($currency)) {
            throw new InvalidCurrencyException($currency);
        }
    }

    /**
     * @return int
     */
    public function getScale(): int
    {
        if (self::CURRENCIES[$this->getName()] ?? false) {
            return 8;
        }

        return 2;
    }

    /**
     * @return BigDecimal
     * @throws InvalidArgumentException
     * @throws ArithmeticException
     */
    public function getMinimumAmount(): BigDecimal
    {
        $scale = $this->getScale();

        $number = 10 ** $scale;

        return BigDecimal::one()->dividedBy($number, $scale, RoundingMode::DOWN);
    }

